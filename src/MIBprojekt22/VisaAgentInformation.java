    private void hamtaAgenter(){
        try {
            ArrayList<String> agenter = new ArrayList<>();
            agenter = idb.fetchColumn("SELECT Namn FROM Agent");
            for (String namn : agenter){
                CBvaljagent.addItem(namn);
               
            }
        } catch (InfException ettUndantag){
            JOptionPane.showMessageDialog(null, "N책got har g책tt fel!");
        }
    }
    
    public void setInfo(){
        HashMap<String, String> info;
        info = getInfoAgent();
                
           String namn = info.get("Namn");
           String telnmr = info.get("Telefon");
           String agentID = info.get("Agent_ID");
           String omrade = info.get("Omrade");
           String ansDatum = info.get("Anstallningsdatum");
        
        LBnamn.setText(namn);
        LBtelnmr.setText(telnmr);
        LBagentID.setText(agentID);
        LBomradeID.setText(omrade);
        LBregdatum.setText(ansDatum);
    }
    
    public HashMap getInfoAgent(){
        HashMap<String,String> info = new HashMap<>();
        try {
            info = idb.fetchRow("SELECT Agent_ID, Agent.Namn, Telefon, Anstallningsdatum, Omrade FROM Agent WHERE Agent.Namn = '" + CBvaljagent.getSelectedItem().toString() + "';");
        } catch (InfException ettUndantag){
            JOptionPane.showMessageDialog(null, "N책got har g책tt fel!"); 
        }
        return info;
    }
    
    
